using System;

namespace Problem2.DoubleFloat
{
    class DoubleFloat
    {
        static void Main()
        {
            double firstValue = 34.567839023;
            float secondValue = 12.345f;
            double thirdValue = 8923.1234857;
            float fourthValue = 3456.091f;

            Console.WriteLine("First Value is: {0}", firstValue);
            Console.WriteLine("Second Value is: {0}", secondValue);
            Console.WriteLine("Third Value is: {0}", thirdValue);
            Console.WriteLine("Fourth Value is: {0}", fourthValue);
        }
    }
}
